#

#
# Zadanie 2.
# Zweryfikować istnienie i zawartość pliku `drugi` z katalogu `ddd`.
# Plik powinien zawierać napis `Ala ma kota.`. Jeśli czegoś brakuje
# to odpowiedni plik stworzyć lub nadpisać istniejący.
#

FILE="ddd/drugi.txt"
TEXT="Ala ma kota"

if [ ! -f "$FILE" ]; then
	echo plik nie istnieje...tworze plik
	echo "$TEXT" > "$FILE"
else
	echo plik istnieje...sprawdzam zawartosc
	if ! grep -q "$TEXT" "$FILE"; then
		echo plik nie zawiera tekstu...dolaczam tekst
		echo "$TEXT" > "$FILE"
	else
		echo plik zawiera tekst. Nie ma nic do roboty
	fi
fi
